# Boolean Operators

# AND
# OR
# NOT

# Condition 1 (AND)
if (10 > 20) and (20 > 30):
    print("True")
else:
    print("False")

# Condition 2 (OR)
if (10 < 20) or (20 > 30):
    print("True")
else:
    print("False")

# Condition 3 (NOT)
if not (10 > 20):
    print("True")
else:
    print("False")

# Condition 4 (NAND)
if not((10 > 20) and (20 > 30)):
    print("True")
else:
    print("False")